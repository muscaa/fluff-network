package fluff.network.client;

/**
 * Enum representing the different types of client errors.
 */
public enum ClientErrorType {
    
    /**
     * Error type for connection-related issues.
     */
    CONNECTION,
    
    /**
     * Error type for read-related issues.
     */
    READ,
    
    /**
     * Error type for write-related issues.
     */
    WRITE;
}
